    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        double promFrente=0,promFondo=0,totalArea=0,totalPerimetro=0;
        
        Local loc = new Local();
        
        for (int r = 0; r < jTableDimensiones.getRowCount(); r++) 
        {
            
            loc.setNombre(jTableDimensiones.getValueAt(r,0)+"");                           
            loc.setFondo(Double.parseDouble(jTableDimensiones.getValueAt(r, 2)+""));                             
            loc.setPerimetro(Double.parseDouble(jTableDimensiones.getValueAt(r, 4)+""));
            
            jTableDimensiones.setValueAt(loc.frente(), r, 1);
            loc.setFrente(Double.parseDouble(jTableDimensiones.getValueAt(r, 1)+ ""));
            
            jTableDimensiones.setValueAt(loc.area(), r, 3);
            loc.setArea(Double.parseDouble(jTableDimensiones.getValueAt(r, 3)+ ""));
                
            promFrente += loc.getFrente();
            promFondo += loc.getFondo();
            totalArea += loc.getArea();
            totalPerimetro += loc.getPerimetro();
        }
        
        promFrente /= jTableDimensiones.getRowCount();
        promFondo /= jTableDimensiones.getRowCount();
        
        jLabPromFrente.setText(df.format(promFrente)+"");
        jLabPromFondo.setText(df.format(promFondo)+"");
        jLabTotalArea.setText(df.format(totalArea)+"");
        jLabTotalPerimetro.setText(df.format(totalPerimetro)+"");
        
    }//GEN-LAST:event_jButCalcularActionPerformed

