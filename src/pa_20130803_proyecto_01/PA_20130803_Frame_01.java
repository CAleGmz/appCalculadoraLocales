    private void jButOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOrdenarActionPerformed
        // TODO add your handling code here:
        Local[] dimension = new Local[jTableDimensiones.getRowCount()];
        
        for (int r = 0; r < jTableDimensiones.getRowCount(); r++) 
        {           
            Local loc = new Local();
            
            loc.setNombre(jTableDimensiones.getValueAt(r,0)+"");            
            loc.setFrente(Double.parseDouble(jTableDimensiones.getValueAt(r, 1)+""));                
            loc.setFondo(Double.parseDouble(jTableDimensiones.getValueAt(r, 2)+""));                
            loc.setArea(Double.parseDouble(jTableDimensiones.getValueAt(r, 3)+""));                
            loc.setPerimetro(Double.parseDouble(jTableDimensiones.getValueAt(r, 4)+""));
            
            dimension[r] = loc;
        }
        
        if(jCheckBoxAlfabetico.isSelected())
        {
            for (int i = 0; i < dimension.length - 1; i++) 
                for (int j = i+1; j < dimension.length; j++)                 
                    if(dimension[i].getNombre().compareTo(dimension[j].getNombre()) > 0)
                    {
                        Local aux = dimension[i];
                        dimension[i]= dimension[j];
                        dimension[j]= aux;
                    }
        }
        
        if(jCheckBoxAsendenteFrente.isSelected())
        {
            for (int i = 0; i < dimension.length - 1; i++) 
                for (int j = i+1; j < dimension.length; j++)                 
                    if(dimension[i].getFrente() > dimension[j].getFrente())
                    {
                        Local aux = dimension[i];
                        dimension[i]= dimension[j];
                        dimension[j]= aux;
                    }
        }
        
        if(jCheckBoxDescendenteArea.isSelected())
        {
            for (int i = 0; i < dimension.length - 1; i++) 
                for (int j = i+1; j < dimension.length; j++)                 
                    if(dimension[i].getArea() < dimension[j].getArea())
                    {
                        Local aux = dimension[i];
                        dimension[i]= dimension[j];
                        dimension[j]= aux;
                    }
        }
        
        for(int r = 0; r < dimension.length; r++) 
        {
            jTableDimensiones.setValueAt(dimension[r].getNombre(), r, 0);
            jTableDimensiones.setValueAt(dimension[r].getFrente(), r, 1);
            jTableDimensiones.setValueAt(dimension[r].getFondo(), r, 2);
            jTableDimensiones.setValueAt(dimension[r].getArea(), r, 3);
            jTableDimensiones.setValueAt(dimension[r].getPerimetro(), r, 4);
        }
                
    }//GEN-LAST:event_jButOrdenarActionPerformed

    private void jButMayorMenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMayorMenorActionPerformed
        // TODO add your handling code here:        
        Local mayorFrente = new Local();        
        Local menorFrente = new Local();
        
        Local mayorArea = new Local();        
        Local menorArea = new Local();
        
        for (int r = 0; r < jTableDimensiones.getRowCount(); r++) 
        {
            
            Local loc = new Local();
            
            loc.setNombre(jTableDimensiones.getValueAt(r,0)+"");                           
            loc.setFondo(Double.parseDouble(jTableDimensiones.getValueAt(r, 2)+""));                             
            loc.setPerimetro(Double.parseDouble(jTableDimensiones.getValueAt(r, 4)+""));
            
            loc.setFrente(loc.frente());           
            
            if(r==0)
            {
                mayorFrente = loc;
                menorFrente = loc;
                mayorArea = loc;
                menorArea = loc; 
            }
            
            if(loc.frente() > mayorFrente.frente())
                mayorFrente = loc;
            
            if(loc.frente() < menorFrente.frente())
                menorFrente = loc;
            
            if(loc.area() > mayorArea.area())
                mayorArea = loc;
            
            if(loc.area() < menorArea.area())
                menorArea = loc;
            
        }
        
        jLabMayorFrente.setText("Quien tiene el mayor frente es " + mayorFrente.getNombre());
        jLabMenorFrente.setText("Quien tiene el menor frente es " + menorFrente.getNombre());        
        jLabMayorArea.setText("Quien tiene la mayor area es " + mayorArea.getNombre());
        jLabMenorArea.setText("Quien tiene la menor area es " + menorArea.getNombre());
        
    }//GEN-LAST:event_jButMayorMenorActionPerformed

